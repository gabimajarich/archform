// Priority band helper — shared with the scoring function
export const BANDS = { low: [0, 33], mid: [34, 66], high: [67, 100] };

export function priorityStrength(v, band) {
  if (band === "low")  return v <= 33 ? (33 - v) / 33 : 0;
  if (band === "high") return v >= 67 ? (v - 67) / 33 : 0;
  if (band === "mid")  return v >= 34 && v <= 66 ? 1 - Math.abs(50 - v) / 16 : 0;
  return 0;
}
